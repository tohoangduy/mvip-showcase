package com.mq.myvtg.util;

public class ContactInfo {
    public String id;
    public String name;
    public String phone;
    public String email;

    public ContactInfo(String id, String name, String phone, String email) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.email = email;
    }
}
